"""
Aggregate and compare 1-GPU and 2-GPU training metrics.

This script reads per-epoch CSV logs generated by train_ddp_kaggle.py and
produces a compact JSON summary tailored to the assignment rubric.
"""

import argparse
import json
from pathlib import Path

import pandas as pd


def load_optional_total_time(csv_path: str) -> float | None:
    """
    Try to infer a run-level total time from the sibling summary.json file.

    Returns None if the summary file is unavailable. This keeps the script
    robust: it still works even when only the CSV logs are present.
    """
    csv_path = Path(csv_path)
    summary_path = csv_path.parent / "summary.json"
    if not summary_path.exists():
        return None
    payload = json.loads(summary_path.read_text(encoding="utf-8"))
    return float(payload["total_train_time_sec"])


def main():
    parser = argparse.ArgumentParser(description="Analyze 1-GPU vs 2-GPU CIFAR-10 DDP results")
    parser.add_argument("--baseline", required=True, help="Path to 1-GPU epoch_metrics.csv")
    parser.add_argument("--parallel", required=True, help="Path to 2-GPU epoch_metrics.csv")
    parser.add_argument("--output", default="analysis_summary.json")
    args = parser.parse_args()

    base = pd.read_csv(args.baseline)
    par = pd.read_csv(args.parallel)

    # Response time per epoch (required by the brief) is estimated using the
    # average wall-clock epoch duration from each run.
    t1 = float(base["epoch_time_sec"].mean())
    t2 = float(par["epoch_time_sec"].mean())

    # Best validation/test accuracy is the relevant comparison for the accuracy gap.
    acc1 = float(base["test_acc"].max())
    acc2 = float(par["test_acc"].max())

    # This communication fraction is an approximation derived from the training
    # script's communication-dominated timing proxy.
    comm_fraction = float(par["comm_fraction"].mean())

    speedup = t1 / t2
    efficiency = speedup / 2.0 * 100.0
    accuracy_gap = acc1 - acc2

    payload = {
        "avg_epoch_time_1gpu_sec": round(t1, 4),
        "avg_epoch_time_2gpu_sec": round(t2, 4),
        "speedup_2gpu_vs_1gpu": round(speedup, 4),
        "parallel_efficiency_percent": round(efficiency, 2),
        "avg_comm_fraction_2gpu": round(comm_fraction, 4),
        "best_acc_1gpu": round(acc1, 4),
        "best_acc_2gpu": round(acc2, 4),
        "accuracy_gap": round(accuracy_gap, 4),
    }

    total_time_1 = load_optional_total_time(args.baseline)
    total_time_2 = load_optional_total_time(args.parallel)
    if total_time_1 is not None:
        payload["total_train_time_1gpu_sec"] = round(total_time_1, 4)
    if total_time_2 is not None:
        payload["total_train_time_2gpu_sec"] = round(total_time_2, 4)

    Path(args.output).write_text(json.dumps(payload, indent=2), encoding="utf-8")
    print(json.dumps(payload, indent=2))


if __name__ == "__main__":
    main()
